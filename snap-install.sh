## Communication Tools ##

sudo snap install signal-desktop
sudo snap install skype
sudo snap install slack
sudo snap install slack-term


## Developer Tools ##

sudo snap install android-studio --classic
sudo snap install netbeans --classic
sudo snap install pycharm-community


## Misc Snaps ##

sudo snap install ghex-udt       # GHex - Hex Editor
sudo snap install gnome-twitch   # Twitch client
sudo snap install googler        # Googler - Google Web, News and Search from CLI
sudo snap install iagno          # Othello / Reversi
sudo snap install mycroft        # MyCroft
